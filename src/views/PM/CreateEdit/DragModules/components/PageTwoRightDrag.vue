      condition: '',
      conditionObj: [], // 筛选条件数据
      // 内弹窗数据
      innerVisible: false
    }
  },
  props: {
    tabIndex: {
      type: Number,
      dafault: 0
    }
  },
  computed: {
    ...mapGetters(['theme', 'userInfo', 'dataSourceValue', 'dragData'])
  },
  watch: {
    firstdatas: {
      handler (newName, oldName) {
        // console.log(newName)
      },
      immediate: true,
      deep: true
    },
    tabIndex (newVal, oldVal) {
      // console.log(newVal)
      this.firstdatas = []
      if (this.dragData[newVal]) {
        this.firstdatas = this.dragData[newVal]
      }
    }
  },
  components: { draggable },
  created () {},
  mounted () {},
  destroyed () {},
  methods: {
    ...mapMutations(['projectsMangement/storedragdata']),
    handleChange (val) {
      // console.log(val.index)
    },
    changeFn (index, idx) {
      this.firstdatas[index].child[idx].edits = true
      this['projectsMangement/storedragdata']({
        data: this.firstdatas,
        index: this.tabIndex
      })
    },
    focusFn () {},
    editButton (itm, index, idx) {
      this.firstdatas[index].child[idx].edits = false
      this['projectsMangement/storedragdata']({
        data: this.firstdatas,
        index: this.tabIndex
      })
    },
    getButton (itm, index, idx) {
      this.outerVisible = true
    },
    delButton (index, idx) {
      this.firstdatas[index].child.splice(idx, 1)
      if (this.firstdatas[index].child.length === 0) {
        this.firstdatas.splice(index, 1)
      }
      this['projectsMangement/storedragdata']({
        data: this.firstdatas,
        index: this.tabIndex
      })
    },
    delAll (index) {
      this.firstdatas.splice(index, 1)
      this['projectsMangement/storedragdata']({
        data: this.firstdatas,
        index: this.tabIndex
      })
    },
    mouseEnter (val) {
      this.del = val
    },
    mouseLeave () {
      this.del = 888
    },
    toChange (val) {
      if (val?.added?.element) {
        const v = val?.added?.element
        // console.log(v)
        var fn = this.firstdatas.findIndex(
          (item) => item.parentId === v.parentId
        )
        if (fn !== -1) {
          this.firstdatas[fn].child.push({
            id: v.id, // 三级字典对应 id
            dataItemName: v.dataItemName, // 三级字典对应名称
            secondId: v.parentId, // 二级字典对应id
            secondName: v.parentName2, // 二级字典对应名称
            types: v.dataItemName, // 被修改的名称
            rayingStatus: v.rayingStatus, // 映射修改的值
            edits: true
          })
        } else {
          const obj = {
            parentId: v.parentId,
            parentName: v.parentName,
            parentName1: v.parentName1,
            parentName2: v.parentName2,
            child: []
          }
          obj.child.push({
            id: v.id, // 三级字典对应 id
            dataItemName: v.dataItemName, // 三级字典对应名称
            secondId: v.parentId, // 二级字典对应id
            secondName: v.parentName2, // 二级字典对应名称
            types: v.dataItemName, // 被修改的名称
            rayingStatus: v.rayingStatus, // 映射修改的值
            edits: true
          })
          this.firstdatas.push(obj)
          this.activeName.push(obj.parentId)
        }
        // console.log(this.firstdatas)
        this['projectsMangement/storedragdata']({
          data: this.firstdatas,
          index: this.tabIndex
        })
        console.log(this.firstdatas)
      }
    },
    handleIconClick () {
      this.innerVisible = true
    },
    // 添加筛选条件
    addButton () {
      this.conditionObj.push({
        stateType1: '',
        stateType2: '',
        stateType3: ''
      })
    },
    // 删除筛选条件
    deleteButton (index) {
      this.conditionObj.splice(index, 1)
    },
    // 内层弹窗
    innerButton () {}
  }
}
</script>
